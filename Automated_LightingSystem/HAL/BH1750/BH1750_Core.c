/******************************************************************************
* \Syntax          : void BH1750_Init(void)       
* \Description     : BH1950 module initialization
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : parameterName   Parameter Describtion                     
* \Parameters (out): None                                                      
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK                                  
*******************************************************************************/
void BH1750_Init(void)
{
	I2C_Init();
	I2C_StartCondition();
	I2C_SendAddress_WriteOperation(BH1750_ModeAddr);
	I2C_WriteByte(BH1750_ModeInstruction);
	I2C_StopCondition();
	#if (BH1750_ModeInstruction == H_Resolution_Mode_INSTRUCTION)
        _delay_ms(180);
	#elif (BH1750_ModeInstruction == L_Resolution_Mode_INSTRUCTION)
	_delay_ms(16);
    #endif
	
}

/******************************************************************************
* \Syntax          : uint16 BH1750_ReadData(void)
* \Description     : Read the data of measurement from the module
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : parameterName   Parameter Describtion
* \Parameters (out): None
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK
*******************************************************************************/
uint16 BH1750_ReadData(void)
{	
	uint16 res=0;
	I2C_StartCondition();
	I2C_SendAddress_ReadOperation(BH1750_ModeAddr);
	res=I2C_ReadByteAck();
	_delay_ms(180);
	res= ((res << 8 ) | I2C_ReadByteNack());
	I2C_StopCondition();
	
	return res;
}
